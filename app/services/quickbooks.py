from __future__ import annotations

from dataclasses import dataclass
from datetime import date, datetime
from decimal import Decimal, InvalidOperation
import hashlib
import json
from typing import Any


@dataclass(frozen=True)
class QuickBooksClearingConfig:
    clearing_account_ref: str = "Custom App Clearing Account"
    sales_income_account_ref: str = "Marketplace Sales"
    shipping_income_account_ref: str = "Shipping Income"
    ebay_customer_ref: str = "eBay Sales Customer"
    ebay_vendor_ref: str = "eBay Vendor"
    ebay_payment_method_ref: str = "eBay Payout"
    ebay_fee_expense_account_ref: str = "Merchant Account Fees"
    ebay_shipping_expense_account_ref: str = "eBay Shipping Expense"
    ebay_subscription_expense_account_ref: str = "eBay Subscription Expense"
    ebay_adjustment_expense_account_ref: str = "Bank Charges & Fees"
    tax_code_ref: str = "NON"
    doc_number_prefix: str = "GS"


def _field(row: Any, *names: str, default: Any = None) -> Any:
    if isinstance(row, dict):
        for name in names:
            if name in row and row.get(name) is not None:
                return row.get(name)
        return default
    for name in names:
        value = getattr(row, name, None)
        if value is not None:
            return value
    return default


def _money(value: Any) -> str:
    try:
        amount = Decimal(str(value if value is not None else "0"))
    except (InvalidOperation, ValueError):
        amount = Decimal("0")
    return f"{amount.quantize(Decimal('0.01'))}"


def _int(value: Any, default: int = 0) -> int:
    try:
        return int(value)
    except (TypeError, ValueError):
        return default


def _date_text(value: Any) -> str:
    if isinstance(value, datetime):
        return value.date().isoformat()
    if isinstance(value, date):
        return value.isoformat()
    text = str(value or "").strip()
    if not text:
        return date.today().isoformat()
    if "T" in text:
        text = text.split("T", 1)[0]
    return text[:10]


def _ref(value: str) -> dict[str, str]:
    clean = str(value or "").strip()
    return {"value": clean}


def _doc_number(prefix: str, kind: str, source_id: Any) -> str:
    raw_source = str(source_id or "").strip() or "unknown"
    raw = f"{prefix}-{kind}-{raw_source}"
    if len(raw) <= 21:
        return raw
    digest = hashlib.sha1(raw.encode("utf-8")).hexdigest()[:8].upper()
    return f"{prefix}-{kind[:3]}-{digest}"[:21]


def quickbooks_sales_receipt_payload(
    sale: Any,
    *,
    config: QuickBooksClearingConfig | None = None,
) -> dict[str, Any]:
    cfg = config or QuickBooksClearingConfig()
    source_id = _field(sale, "external_order_id", "order_id", "sale_id", "id", default="")
    quantity = max(1, _int(_field(sale, "quantity_sold", "quantity", "qty", default=1), 1))
    gross_total = Decimal(_money(_field(sale, "gross_amount", "gross_sales", "amount", "total", default="0")))
    unit_price = gross_total / Decimal(quantity)
    sku = str(_field(sale, "sku", "product_sku", "item_ref", default="")).strip()

    return {
        "DocNumber": _doc_number(cfg.doc_number_prefix, "SALE", source_id),
        "TxnDate": _date_text(_field(sale, "transaction_date", "sold_at", "created_at", default=None)),
        "CustomerRef": _ref(cfg.ebay_customer_ref),
        "PaymentMethodRef": _ref(cfg.ebay_payment_method_ref),
        "DepositToAccountRef": _ref(cfg.clearing_account_ref),
        "Line": [
            {
                "DetailType": "SalesItemLineDetail",
                "Amount": _money(gross_total),
                "SalesItemLineDetail": {
                    "ItemRef": _ref(sku),
                    "Qty": quantity,
                    "UnitPrice": _money(unit_price),
                    "TaxCodeRef": _ref(cfg.tax_code_ref),
                },
            }
        ],
        "PrivateNote": "Generated by GoldenStackers app using clearing-account workflow.",
    }


def quickbooks_order_fee_purchase_payload(
    fee: Any,
    *,
    config: QuickBooksClearingConfig | None = None,
) -> dict[str, Any]:
    cfg = config or QuickBooksClearingConfig()
    source_id = _field(fee, "external_order_id", "order_id", "sale_id", "id", default="")
    return {
        "DocNumber": _doc_number(cfg.doc_number_prefix, "FEE", source_id),
        "TxnDate": _date_text(_field(fee, "fee_date", "transaction_date", "sold_at", "created_at", default=None)),
        "AccountRef": _ref(cfg.clearing_account_ref),
        "EntityRef": _ref(cfg.ebay_vendor_ref),
        "PaymentType": "Cash",
        "Line": [
            {
                "DetailType": "AccountBasedExpenseLineDetail",
                "Amount": _money(_field(fee, "fee_amount", "fees", "fee_total", default="0")),
                "AccountBasedExpenseLineDetail": {
                    "AccountRef": _ref(cfg.ebay_fee_expense_account_ref),
                },
            }
        ],
        "PrivateNote": "Generated by GoldenStackers app for eBay order fees via clearing account.",
    }


def quickbooks_non_order_fee_purchase_payload(
    adjustment: Any,
    *,
    config: QuickBooksClearingConfig | None = None,
) -> dict[str, Any]:
    cfg = config or QuickBooksClearingConfig()
    fee_type = str(_field(adjustment, "fee_type", "type", "category", default="adjustment") or "").strip().lower()
    if "ship" in fee_type or "label" in fee_type:
        expense_account = cfg.ebay_shipping_expense_account_ref
    elif "subscription" in fee_type or "store" in fee_type:
        expense_account = cfg.ebay_subscription_expense_account_ref
    else:
        expense_account = cfg.ebay_adjustment_expense_account_ref
    source_id = _field(adjustment, "external_id", "payout_id", "id", default=fee_type)
    return {
        "DocNumber": _doc_number(cfg.doc_number_prefix, "ADJ", source_id),
        "TxnDate": _date_text(_field(adjustment, "fee_date", "transaction_date", "created_at", default=None)),
        "AccountRef": _ref(cfg.clearing_account_ref),
        "EntityRef": _ref(cfg.ebay_vendor_ref),
        "PaymentType": "Cash",
        "Line": [
            {
                "DetailType": "AccountBasedExpenseLineDetail",
                "Amount": _money(_field(adjustment, "amount", "fee_amount", "total", default="0")),
                "AccountBasedExpenseLineDetail": {
                    "AccountRef": _ref(expense_account),
                },
            }
        ],
        "PrivateNote": f"Generated by GoldenStackers app for eBay payout {fee_type or 'adjustment'} via clearing account.",
    }


