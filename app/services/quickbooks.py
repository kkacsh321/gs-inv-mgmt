def quickbooks_payload_validation_issues(payload: dict[str, Any]) -> list[str]:
    issues: list[str] = []
    if not str(payload.get("DocNumber") or "").strip():
        issues.append("missing_doc_number")
    if not str(payload.get("TxnDate") or "").strip():
        issues.append("missing_txn_date")
    for ref_key in ("CustomerRef", "DepositToAccountRef", "AccountRef", "EntityRef"):
        ref = payload.get(ref_key)
        if ref is not None and not str((ref or {}).get("value") or "").strip():
            issues.append(f"missing_{ref_key.lower()}")
    for index, line in enumerate(payload.get("Line") or []):
        detail_type = str(line.get("DetailType") or "").strip()
        if not detail_type:
            issues.append(f"line_{index}_missing_detail_type")
        if Decimal(_money(line.get("Amount"))) <= 0:
            issues.append(f"line_{index}_nonpositive_amount")
        detail = line.get(detail_type) or {}
        item_ref = detail.get("ItemRef")
        account_ref = detail.get("AccountRef")
        if item_ref is not None and not str((item_ref or {}).get("value") or "").strip():
            issues.append(f"line_{index}_missing_item_ref")
        if account_ref is not None and not str((account_ref or {}).get("value") or "").strip():
            issues.append(f"line_{index}_missing_expense_account_ref")
    return issues


def quickbooks_rows_from_actual_economics(actual_rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for row in actual_rows or []:
        sale_id = _field(row, "sale_id", "id", default="")
        sold_at = _field(row, "sold_at", "transaction_date", "created_at", default="")
        doc_number = str(_field(row, "external_order_id", "doc_number", default="") or "").strip()
        if not doc_number:
            doc_number = f"SALE-{sale_id or 'unknown'}"
        rows.append(
            {
                "txn_date": _date_text(sold_at),
                "doc_number": doc_number,
                "sale_id": sale_id,
                "marketplace": str(_field(row, "marketplace", default="") or "").strip().lower(),
                "item_sku": str(_field(row, "sku", "item_sku", default="") or "").strip(),
                "item_description": str(_field(row, "product_title", "item_description", default="") or "").strip(),
                "quantity": max(1, _int(_field(row, "qty", "quantity", "quantity_sold", default=1), 1)),
                "amount": _money(_field(row, "sold_price", "gross_sales", "amount", default="0")),
                "fees": _money(_field(row, "allocated_fee_actual", "fees", default="0")),
                "fee_source": str(_field(row, "actual_fee_source", "fee_source", default="") or "").strip(),
                "shipping_cost": _money(
                    _field(row, "allocated_shipping_charged", "shipping_cost", default="0")
                ),
                "shipping_label_cost": _money(
                    _field(row, "allocated_shipping_actual", "shipping_label_cost", default="0")
                ),
                "shipping_label_source": str(
                    _field(row, "actual_shipping_source", "shipping_label_source", default="") or ""
                ).strip(),
            }
        )
    return rows


def quickbooks_payload_preview_records(
    qbo_rows: list[dict[str, Any]],
    *,
    config: QuickBooksClearingConfig | None = None,
) -> list[dict[str, Any]]:
    records: list[dict[str, Any]] = []
    for row in qbo_rows or []:
        source_doc = str(_field(row, "doc_number", default="") or "").strip()
        sale_payload = quickbooks_sales_receipt_payload(
            {
                "external_order_id": source_doc,
                "sold_at": _field(row, "txn_date", default=""),
                "sku": _field(row, "item_sku", default=""),
                "quantity_sold": _field(row, "quantity", default=1),
                "gross_amount": _field(row, "amount", default="0"),
            },
            config=config,
        )
        records.append(_preview_record("SalesReceipt", source_doc, "sales_receipt", sale_payload))

        fee_amount = Decimal(_money(_field(row, "fees", default="0")))
        if fee_amount > 0:
            fee_payload = quickbooks_order_fee_purchase_payload(
                {
                    "external_order_id": source_doc,
                    "fee_date": _field(row, "txn_date", default=""),
                    "fee_amount": fee_amount,
                },
                config=config,
            )
            records.append(_preview_record("Purchase", source_doc, "order_fee_purchase", fee_payload))

        label_amount = Decimal(_money(_field(row, "shipping_label_cost", default="0")))
        if label_amount > 0:
            label_payload = quickbooks_non_order_fee_purchase_payload(
                {
                    "external_id": f"{source_doc or 'unknown'}-SHIP",
                    "fee_type": "shipping_label",
                    "fee_date": _field(row, "txn_date", default=""),
                    "amount": label_amount,
                },
                config=config,
            )
            records.append(_preview_record("Purchase", source_doc, "shipping_label_purchase", label_payload))
    return records


def quickbooks_manual_journal_packet(
    actual_rows: list[dict[str, Any]],
    *,
    config: QuickBooksClearingConfig | None = None,
    period_label: str = "",
) -> dict[str, Any]:
    cfg = config or QuickBooksClearingConfig()
    qbo_rows = quickbooks_rows_from_actual_economics(actual_rows)
    gross_sales = sum((Decimal(_money(_field(row, "amount", default="0"))) for row in qbo_rows), Decimal("0"))
    shipping_charged = sum(
        (Decimal(_money(_field(row, "shipping_cost", default="0"))) for row in qbo_rows), Decimal("0")
    )
    ebay_fees = sum((Decimal(_money(_field(row, "fees", default="0"))) for row in qbo_rows), Decimal("0"))
    label_spend = sum(
        (Decimal(_money(_field(row, "shipping_label_cost", default="0"))) for row in qbo_rows), Decimal("0")
    )
    income_receivable = gross_sales + shipping_charged
    marketplace_deductions = ebay_fees + label_spend
    expected_net_payout = income_receivable - marketplace_deductions

    journal_rows: list[dict[str, Any]] = []

    def add_row(
        *,
        line_no: int,
        account_ref: str,
        debit: Decimal = Decimal("0"),
        credit: Decimal = Decimal("0"),
        memo: str,
        source: str,
    ) -> None:
        if debit <= 0 and credit <= 0:
            return
        journal_rows.append(
            {
                "line_no": line_no,
                "account_ref": account_ref,
                "debit": _money(debit),
                "credit": _money(credit),
                "memo": memo,
                "source": source,
            }
        )

    label = f" for {period_label}" if str(period_label or "").strip() else ""
    add_row(
        line_no=1,
        account_ref=cfg.clearing_account_ref,
        debit=income_receivable,
        memo=f"Record gross eBay receivable{label}: item sales plus buyer-paid shipping.",
        source="gross_sales_plus_shipping_charged",
    )
    add_row(
        line_no=2,
        account_ref=cfg.sales_income_account_ref,
        credit=gross_sales,
        memo=f"Record eBay gross item sales{label}.",
        source="gross_sales",
    )
    add_row(
        line_no=3,
        account_ref=cfg.shipping_income_account_ref,
        credit=shipping_charged,
        memo=f"Record buyer-paid shipping charged through eBay{label}.",
        source="shipping_charged",
    )
    add_row(
        line_no=4,
        account_ref=cfg.ebay_fee_expense_account_ref,
        debit=ebay_fees,
        memo=f"Record eBay order-level marketplace fees{label}.",
        source="ebay_fees",
    )
    add_row(
        line_no=5,
        account_ref=cfg.ebay_shipping_expense_account_ref,
        debit=label_spend,
        memo=f"Record eBay/marketplace shipping-label spend{label}.",
        source="shipping_label_spend",
    )
    add_row(
        line_no=6,
        account_ref=cfg.clearing_account_ref,
        credit=marketplace_deductions,
        memo=f"Reduce clearing account for eBay fees and shipping-label deductions{label}.",
        source="marketplace_deductions",
    )

    total_debits = sum((Decimal(row["debit"]) for row in journal_rows), Decimal("0"))
    total_credits = sum((Decimal(row["credit"]) for row in journal_rows), Decimal("0"))
    summary = {
        "period_label": str(period_label or "").strip(),
        "source_rows": len(qbo_rows),
        "gross_sales": _money(gross_sales),
        "shipping_charged": _money(shipping_charged),
        "income_receivable": _money(income_receivable),
        "ebay_fees": _money(ebay_fees),
        "shipping_label_spend": _money(label_spend),
        "marketplace_deductions": _money(marketplace_deductions),
        "expected_net_payout": _money(expected_net_payout),
        "total_debits": _money(total_debits),
        "total_credits": _money(total_credits),
        "out_of_balance": _money(total_debits - total_credits),
    }
    packet = {"summary": summary, "journal_rows": journal_rows}
    packet["evidence_sha256"] = hashlib.sha256(json.dumps(packet, sort_keys=True).encode("utf-8")).hexdigest()
    return packet


def _preview_record(
    payload_type: str,
    source_doc_number: str,
    action: str,
    payload: dict[str, Any],
) -> dict[str, Any]:
    issues = quickbooks_payload_validation_issues(payload)
    return {
        "payload_type": payload_type,
        "action": action,
        "source_doc_number": str(source_doc_number or "").strip(),
        "qbo_doc_number": str(payload.get("DocNumber") or "").strip(),
        "validation_status": "ok" if not issues else "review",
        "validation_issues": issues,
        "payload_sha256": hashlib.sha256(json.dumps(payload, sort_keys=True).encode("utf-8")).hexdigest(),
        "payload": payload,
    }
